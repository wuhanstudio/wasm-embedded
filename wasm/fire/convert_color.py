# Function to convert RGBA to RGB565
def rgba_to_rgb565(rgba):
    r = rgba[0] >> 3
    g = rgba[1] >> 2
    b = rgba[2] >> 3
    return (r << 11) | (g << 5) | b

# Process the rgba_data list in chunks of 4 bytes (RGBA)
rgb565_data = []
for i in range(0, len(rgba_data), 4):
    rgba = rgba_data[i:i+4]
    rgb565 = rgba_to_rgb565(rgba)
    rgb565_data.append(rgb565)

# Display the converted RGB565 data
hex_output = [f'{color:04X}' for color in rgb565_data]
print(hex_output)